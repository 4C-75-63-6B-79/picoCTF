# We are pasting the entire hex values as strings seprated by spaces.
hexValue = '''0x70 0x69 0x63 0x6f 0x43 0x54 0x46 0x7b 0x34 0x35 0x63 0x31 0x31 0x5f 0x6e 0x30 0x5f 0x71 0x75 0x33 0x35 0x37 0x31 0x30 0x6e 0x35 0x5f 0x31 0x6c 0x6c 0x5f 0x74 0x33 0x31 0x31 0x5f 0x79 0x33 0x5f 0x6e 0x30 0x5f 0x6c 0x31 0x33 0x35 0x5f 0x34 0x34 0x35 0x64 0x34 0x31 0x38 0x30 0x7d '''

# We are then spliting the hexValue string where there is space which gives us a list of the hex values.
hexValuelist = hexValue.split(" ");
print(hexValuelist);

# We run a for loop on the hex number list and convert them into ascii and then convert them into the char value and print them.
for hexValue in hexValuelist:
    if(hexValue.strip() == ''):
        continue;
    print(chr(int(hexValue.strip(), 16)), end="");